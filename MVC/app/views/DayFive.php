                    <h1>¡Bingo!</h1>
                </div>
                <hr>
                <div class="contenido_actividad">
                    <div class="subtema_actividad">
                        <p> El juego de Bingo dio inicio alrededor de las 9:00 a.m. La dinámica consistía en que los interesados en participar debían comprar créditos
                            dentro del programa desarrollado por los compañeros: 100 créditos tenían un costo de $1 dólar. </p>
                        <p> Posteriormente, el presentador —en este caso, Gerson— habilitaba la sala, y los participantes debían
                            ingresar el código correspondiente para poder acceder al juego. </p>
                        <p> Al ingresar, cada jugador elegía los cartones que utilizaría.
                            Estos eran de un único uso, lo que significaba que, una vez jugada la partida,
                            se eliminaban automáticamente del sistema. </p>
                        <p> Al iniciar la partida, se seleccionaba una forma objetivo y comenzaban a aparecer los números.
                            Los participantes debían respetar la figura indicada; de lo contrario, se les penalizaba con 30 segundos.
                            Cuando alguien completaba correctamente la forma, debía gritar “¡Bingo!” y hacer clic en el botón que aparecía en la pantalla
                            para confirmar su victoria. </p>
                        <p>
                            Los ganadores fueron premiados con obsequios como audífonos inalámbricos, powerbanks, camisetas, gorras, 
                            bocinas, entre otros premios; un detalle que añadió aún más emoción a la jornada.
                        </p>

                        <p>También se contó con la presencia de ACOPUS de R.L, quienes llevaron algunos productos para regalar por medio de ciertas dinamicas,
                            como "El primero que diga el nombre del Ingeniero Guandique se lleva el premio" o "Quien traiga un calcetin primero gana".</p>
